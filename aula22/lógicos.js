/* 
Operadores lógicos: 

    && -> and -> e 
    || -> or -> ou
    ! -> not -> não

*/

console.log(true && true && true && true); // Deu true porque todos são 'true', caso um fosse false, daria 'false'.

console.log(false || false); // Deu false porque os dois são 'false', se um fosse true, daria 'true'.

console.log(!false); // Deu true por conta da negação utilizada antes do boolean. Sempre vai dar o valor contrário. 